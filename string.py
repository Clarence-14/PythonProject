text = "Hello world"
course = "WEB DEVELOPMENT"

# Accessing an element in a string.
print(text[0])
print(text[1])

# Determining the size of a string.
print(len(text))

# Modifying a string.
print(text.upper())
print(course.lower())

# String concatenation. - Joining strings.
first = "Hello there"
name = "Olivia"
print(first + name)
print(first + " " + name)
